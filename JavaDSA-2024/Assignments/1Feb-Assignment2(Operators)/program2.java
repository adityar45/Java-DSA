//Comparison of numbers using relational operators

class program2 {
    public static void main(String[] args) {
        int num1 = 7;
        int num2 = 5;

        System.out.println("Given numbers are : "+num1+" and "+num2);

        System.out.println("num1 > num2 : "+(num1 > num2));
        System.out.println("num1 < num2 : "+(num1 < num2));
        System.out.println("num1 == num2 : "+(num1 == num2));
        System.out.println("num1 != num2 : "+(num1 != num2));
        System.out.println("num1 >= num2 : "+(num1 >= num2));
        System.out.println("num1 <= num2 : "+(num1 <= num2));

    }
}