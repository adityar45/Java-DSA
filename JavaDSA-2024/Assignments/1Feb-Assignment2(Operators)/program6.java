class program6 {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 5;

        System.out.println(num1+"+=3 : "+(num1+=3));
        System.out.println(num2+"-=2 : "+(num2-=2));
        System.out.println(num1+"*=2 : "+(num1*=2));
        System.out.println(num2+"/=3 : "+(num2/=3));
        System.out.println(num1+"%=5 : "+(num1%=5));
        
    }
}