class Demo3 {
	public static void main(String[] args){
		
		int a = 10;
		int b = -3;
		System.out.println("Positive value for >> and >>>");
		System.out.println("a>> :"+(a >> 2));
		System.out.println("a>>> :"+(a >>> 2));
		System.out.println("a<< :"+(a << 2));
		System.out.println("negative value for >> , << and >>>");
		System.out.println("b>> :"+(b >> 2));
		System.out.println("b>>> :"+(b >>> 2));
		System.out.println("b<< :"+(b << 2));
	}
}
